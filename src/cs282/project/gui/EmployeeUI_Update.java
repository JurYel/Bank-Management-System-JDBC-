    private void jbtn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_UpdateActionPerformed
        try{
            String branch = jComboBox_Branch.getSelectedItem().toString();
            int branchID = branchDAO.getBranchIDByCity(branch);
            String id = jtxt_EmployeeID.getText().trim();
            String first = jtxt_FirstName.getText().trim();
            String last = jtxt_LastName.getText().trim();
            
            
                Employee newEmp = new Employee(id,branch,first,last);
                newEmp.setBranchID(branchID);
                
                
                empDAO.updateEmployee(newEmp);
                empUI.refreshEmployeesTable();
                setVisible(false);
                JOptionPane.showMessageDialog(empUI,"Employee updated successfully!","Update Success",JOptionPane.ERROR_MESSAGE);
                
            
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_UpdateActionPerformed

    private void jbtn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_DeleteActionPerformed
        try{
            String id = jtxt_EmployeeID.getText().trim();
            
            empDAO.fireEmployee(id);
            empUI.refreshEmployeesTable();
            setVisible(false);
            JOptionPane.showMessageDialog(EmployeeUI_Update.this,"Employee has been fired!","Fired",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_DeleteActionPerformed

