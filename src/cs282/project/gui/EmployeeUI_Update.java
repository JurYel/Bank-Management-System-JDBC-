    private void populateGUI(){
        try{
            ArrayList<Branch> branches = branchDAO.getAllBranches();
            
            for(int i = 0;i<branches.size();i++){
                jComboBox_Branch.addItem(branches.get(i).getCity());
            }
            jComboBox_Branch.setSelectedItem(employee.getBranchName());
            jtxt_EmployeeID.setText(employee.getEmployeeID());
            jtxt_FirstName.setText(employee.getFirstName());
            jtxt_LastName.setText(employee.getLastName());
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
