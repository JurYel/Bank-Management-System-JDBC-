    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
        String search = jtxt_Search.getText().trim();
        try{
            if(search.isEmpty()){
                ArrayList<Bank> list = bankDAO.getAllBanks();
                BankTableModel model = new BankTableModel(list);
                jtbl_Banks.setModel(model);
            }
            else{
                ArrayList<Bank> list = bankDAO.searchBank(search);
                BankTableModel model = new BankTableModel(list);
                jtbl_Banks.setModel(model);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jtxt_SearchKeyTyped

