    private void jtbl_BanksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbl_BanksMouseClicked
        int row = jtbl_Banks.getSelectedRow();
        if((evt.getButton() == MouseEvent.BUTTON1) && (evt.getClickCount() == 2)){
            int bankID = Integer.parseInt(jtbl_Banks.getValueAt(row,-2).toString());
            String bankName = jtbl_Banks.getValueAt(row,0).toString();
            String bankCode = jtbl_Banks.getValueAt(row,1).toString();
            String address = jtbl_Banks.getValueAt(row,2).toString();
            
            Bank bank = new Bank(bankName,bankCode,address);
            bank.setBankID(bankID);
            BankUI_Update updateUI = new BankUI_Update(bank,bankDAO,BankUI.this);
            updateUI.setVisible(true);
        }
    }//GEN-LAST:event_jtbl_BanksMouseClicked

