    public void refreshLoansHistory(){
        try{
            int customerID = custDAO.getCustomerID(customerName);
            ArrayList<LoanHistory> list = loanHistDAO.getUserLoans(customerID);
            UserLoanHistoryTableModel model = new UserLoanHistoryTableModel(list);
            jtbl_Loans.setModel(model);
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
    
    public void refreshLoansGUI(double loaned,double amount,String paid, int loan_no){
        jlbl_Loaned.setText(String.valueOf(loaned));
                jlbl_Balance.setText(String.valueOf(amount));
                jlbl_LastPaid.setText(paid);
                if(loan_no == -1){
                    jlbl_LoanNumber.setText("No Loans");
                }
                else{
                    jlbl_LoanNumber.setText(String.valueOf(loan_no));
                }
                
    }
    
    private void populateGUI(){
        try{
            int customerID = custDAO.getCustomerID(customerName);
            int loan_no = loanDAO.getLoanID(customerID);
            double amount = loanDAO.getLoanAmount(loan_no);
            double bal = loanDAO.getLoanBalance(loan_no);
            
            ArrayList<String> paytimes = loanHistDAO.getLastPaidByLoanNo(loan_no);
            
            jlbl_Customer.setText(customerName);
            jlbl_Loaned.setText(String.valueOf(amount));
            jlbl_Balance.setText(String.valueOf(bal));
            if(paytimes.size() > 0){
                jlbl_LastPaid.setText(paytimes.get(0));
            }
            else{
                jlbl_LastPaid.setText("No Record");
            }
            
            if(loan_no == 0){
                jlbl_LoanNumber.setText("No Loans");
            }
            else{
                jlbl_LoanNumber.setText(String.valueOf(loan_no));
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
    
