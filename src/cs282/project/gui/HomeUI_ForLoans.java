    private void jbtn_ReimburseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_ReimburseActionPerformed
        try{
            double amount = 0;
            int loan_no = 0;
            double balance = Double.parseDouble(jlbl_Balance.getText());
            double loaned = Double.parseDouble(jlbl_Loaned.getText());
            ArrayList<String> paytimes = new ArrayList<>();
            
            int customerID = custDAO.getCustomerID(customerName);
            ArrayList<String> loan_types = new ArrayList<>();
            
            if(jlbl_Amount.getText().isEmpty()){
                JOptionPane.showMessageDialog(HomeUI_ForLoans.this,"Please provide amount","Reimburse Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                amount = Double.parseDouble(jlbl_Amount.getText().trim());
                if(jlbl_LoanNumber.getText().equals("No Loans")){
                    JOptionPane.showMessageDialog(HomeUI_ForLoans.this,"Cannot reimburse without any loans","Reimburse Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    loan_no = Integer.parseInt(jlbl_LoanNumber.getText());
                    loan_types = loanHistDAO.getLoanType(loan_no);
                    paytimes = loanHistDAO.getLastPaidByLoanNo(loan_no);
                    if(loanDAO.checkIfLoanPaid(loan_no)){
                        JOptionPane.showMessageDialog(HomeUI_ForLoans.this,"Loan is already paid last " + paytimes.get(0),"Payment Error",JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        if(amount == loaned){
                            loanDAO.fullLoanPayment(loan_no);

                            LoanHistory loanHist = new LoanHistory(null,null,"Full Payment",amount,null);
                            loanHist.setLoanNo(loan_no);
                            loanHist.setCustomerID(customerID);
                            loanHist.setLoanType(loan_types.get(0));

                            loanHistDAO.recordAction(loanHist);
                            this.refreshLoansHistory();
                            this.refreshLoansGUI(0.0,0.0,paytimes.get(0),-1);
                        }
                        else if(amount <= balance){
                            loanDAO.reimburseLoan(loan_no, (balance - amount));

                            LoanHistory loanHist = new LoanHistory(null,null,"Paid Portion",amount,null);
                            loanHist.setCustomerID(customerID);
                            loanHist.setLoanNo(loan_no);
                            loanHist.setLoanType(loan_types.get(0));

                            loanHistDAO.recordAction(loanHist);
                            
                            this.refreshLoansHistory();
                            
                            if((balance - amount) == 0.0){
                                JOptionPane.showMessageDialog(HomeUI_ForLoans.this,"Loan fully paid!","Reimbursed Credit",JOptionPane.ERROR_MESSAGE);
                                loanDAO.fullLoanPayment(loan_no);
                                this.refreshLoansGUI(0.0,(balance - amount),"No Record", -1);
                            }
                            else{
                                this.refreshLoansGUI(loaned,(balance - amount),paytimes.get(0),loan_no);
                            }
                            
                            
                            
                            
                        }
                    }
                }
                
                reset();
            }
            
            
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_ReimburseActionPerformed

