    private void jbtn_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_RegisterActionPerformed
        try{
            String branchmain = jComboBox_Branch1.getSelectedItem().toString();
            String custFirst = jtxt_CustFirstName.getText().trim();
            String custLast = jtxt_CustLastName.getText().trim();
            String contact = jtxt_CustContactNumber.getText().trim();
            String address = jtxt_CustAddress.getText().trim();
            
            String employeeID = jtxt_EmployeeID.getText().trim();
            String branch = jComboBox_Branch.getSelectedItem().toString();
            int branchID = branchDAO.getBranchByCity(branch);
            String empFirst = jtxt_EmpFirstName.getText().trim();
            String empLast = jtxt_EmpLastName.getText().trim();
            String empPass = jpf_Password.getText().trim();
            
            if(branchmain.equals("Pick a Branch")){
                JOptionPane.showMessageDialog(HomeUI_Register.this,"Please pick what branch the bank is.","Register Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                if(!(custFirst.isEmpty()) && !(custLast.isEmpty())){
                    if(custDAO.checkIfCustomerExists(String.format("%s %s",custFirst,custLast))){
                        /* Set Bank UI for Customers with Accounts visible */
                        int branchmainID = branchDAO.getBranchIDByCity(branchmain);
                        HomeUI_ForLoans loans = new HomeUI_ForLoans(String.format("%s %s",custFirst,custLast),branchmainID);
                        setVisible(false);
                        loans.setVisible(true);
                    }
                    else{
                        Customer customer = new Customer(custFirst,custLast,address,contact);
                        custDAO.addCustomer(customer);

                        /* Set Bank UI for Customers visible */
                        
                        int branchmainID = branchDAO.getBranchIDByCity(branchmain);
                        HomeUI_ForLoans loans = new HomeUI_ForLoans(String.format("%s %s",custFirst,custLast),branchmainID);
                        setVisible(false);
                        loans.setVisible(true);
                    }
                }
                if(!(employeeID.equals("Employee ID")) && !(empPass.equals("Password"))){
                    if(empDAO.checkEmployeeID(employeeID)){
                        JOptionPane.showMessageDialog(HomeUI_Register.this,"Employee ID already exists!","Register Error",JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        Employee employee = new Employee(employeeID,branch,empFirst,empLast);
                        employee.setBranchID(branchID);
                        employee.setPassword(empPass);

                        empDAO.defRegisterEmployee(employee);

                        HomeUI_Login loginUI = new HomeUI_Login();
                        setVisible(false);
                        loginUI.setVisible(true);
                    }
                }
            }
            
            
            
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_RegisterActionPerformed

