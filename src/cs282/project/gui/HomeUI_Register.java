    private void populateGUI(){
        try{
            ArrayList<Branch> list = branchDAO.getAllBranches();
            for(int i = 0;i<list.size();i++){
                jComboBox_Branch.addItem(list.get(i).getCity());
            }
            
            for(int i = 0;i<list.size();i++){
                jComboBox_Branch1.addItem(list.get(i).getCity());
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
