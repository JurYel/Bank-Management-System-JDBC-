    private void jbtn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_UpdateActionPerformed
        try{
            int id = branch.getBranchID();
            String name = jtxt_BankName.getText().trim();
            String city = jtxt_City.getText().trim();
            String code = jtxt_BranchCode.getText().trim();
            
            
           
                Branch branch = new Branch(name,city,code);
                branch.setBranchID(id);
                
                branchDAO.updateBranch(branch);
                branchUI.refreshBranchTable();
                setVisible(false);
                JOptionPane.showMessageDialog(branchUI,"Branch updated successfully!","Update Success",JOptionPane.INFORMATION_MESSAGE);
                
            
            
           
            
            
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_UpdateActionPerformed

    private void jbtn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_DeleteActionPerformed
        try{
            int id = branch.getBranchID();
            
            if(branchDAO.checkBranchTransactions(id)){
                setVisible(false);
                JOptionPane.showMessageDialog(branchUI,"There are bank accounts created \nunable to perform removal.","Delete Error",JOptionPane.ERROR_MESSAGE);
                
            }
            else{
                branchDAO.deleteBranch(id);
                
                setVisible(false);
                branchUI.refreshBranchTable();
                JOptionPane.showMessageDialog(branchUI,"Branch has been delete successfully!","Delete Success",JOptionPane.INFORMATION_MESSAGE);
                
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_DeleteActionPerformed

